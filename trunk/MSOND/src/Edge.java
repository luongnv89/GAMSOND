
public class Edge {
	int key = 0;
	int i = 0;
	int j = 0;
	
	public int getKey() {
		return key;
	}
	public void setKey(int key) {
		this.key = key;
	}
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public int getJ() {
		return j;
	}
	public void setJ(int j) {
		this.j = j;
	}
	public Edge() {
		this.key = 0;
		this.i = 0;
		this.j = 0;
	}
}
